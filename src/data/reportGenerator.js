import jsPDF from 'jspdf';

/**
 * Generates an EcoLens monthly impact report as a real PDF, saved directly
 * to the user's downloads via jsPDF's built-in .save().
 */
export function generateReportPDF({ userName, transactions, totalSpent, totalCO2, greenScore }) {
  const doc = new jsPDF({ unit: 'pt', format: 'a4' });
  const pageWidth = doc.internal.pageSize.getWidth();
  const margin = 48;
  let y = 60;

  // Header band
  doc.setFillColor(15, 46, 29); // forest
  doc.rect(0, 0, pageWidth, 90, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(22);
  doc.text('EcoLens', margin, 45);
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(11);
  doc.text('Monthly Impact Report', margin, 65);

  y = 130;
  doc.setTextColor(26, 26, 23); // ink
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(16);
  doc.text(`${userName}'s Summary`, margin, y);
  y += 10;
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(10);
  doc.setTextColor(120, 120, 110);
  doc.text(new Date().toLocaleDateString('en-IN', { year: 'numeric', month: 'long', day: 'numeric' }), margin, y + 14);

  // Stat boxes
  y += 40;
  const boxWidth = (pageWidth - margin * 2 - 20) / 3;
  const stats = [
    { label: 'Total Spent', value: `Rs. ${totalSpent.toLocaleString('en-IN')}` },
    { label: 'Carbon Footprint', value: `${totalCO2.toFixed(1)} kg CO2` },
    { label: 'Green Score', value: `${greenScore} / 100` },
  ];
  stats.forEach((s, i) => {
    const x = margin + i * (boxWidth + 10);
    doc.setFillColor(239, 234, 220); // paper-dim
    doc.roundedRect(x, y, boxWidth, 60, 6, 6, 'F');
    doc.setTextColor(15, 46, 29);
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(14);
    doc.text(s.value, x + 12, y + 28);
    doc.setTextColor(120, 120, 110);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9);
    doc.text(s.label, x + 12, y + 45);
  });

  // Transaction table
  y += 100;
  doc.setTextColor(15, 46, 29);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(13);
  doc.text('Recent Transactions', margin, y);
  y += 20;

  doc.setFontSize(9);
  doc.setTextColor(120, 120, 110);
  doc.text('DATE', margin, y);
  doc.text('LABEL', margin + 70, y);
  doc.text('CATEGORY', margin + 280, y);
  doc.text('AMOUNT', margin + 380, y);
  doc.text('CO2 (KG)', margin + 450, y);
  y += 8;
  doc.setDrawColor(220, 215, 200);
  doc.line(margin, y, pageWidth - margin, y);
  y += 16;

  doc.setFont('helvetica', 'normal');
  doc.setTextColor(26, 26, 23);
  doc.setFontSize(9);

  const rows = transactions.slice(0, 20); // cap so the PDF stays one tidy page
  rows.forEach((t) => {
    if (y > 760) return; // simple single-page cap for the hackathon version
    doc.text(t.date, margin, y);
    doc.text(String(t.label).slice(0, 35), margin + 70, y);
    doc.text(t.category, margin + 280, y);
    doc.text(`Rs. ${t.amount}`, margin + 380, y);
    doc.text(String(t.co2), margin + 450, y);
    y += 18;
  });

  // Footer
  doc.setTextColor(150, 150, 140);
  doc.setFontSize(8);
  doc.text('Generated by EcoLens — Bug Busters', margin, 800);

  doc.save(`EcoLens-Report-${new Date().toISOString().slice(0, 10)}.pdf`);
}
